    private void btn_loginActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btn_loginActionPerformed
        // TODO add your handling code here:
        if (log == 0) {
            log = 1;
            user_id.add("useradmin");
            password.add("password");
            user_id.add("arga");
            password.add("arga");
            toggleLogin(true);
            btn_cancel.setEnabled(true);
        } else if (log == 1) {
            if (cek_guest.isSelected()) {
                log=2;
                clearLogin();
                toggleLogin(false);
                btn_login.setEnabled(false);
                toggleButtons(true);
                JOptionPane.showMessageDialog(null, "Berhasil login sebagai guest");
            } else {
                String txtName = txt_userid.getText();
                char[] txtPasswordChar = pwd_password.getPassword();
                String txtPassword = new String(txtPasswordChar);
                if (!"".equals(txtName) && !"".equals(txtPassword)) {
                    int IndexId = user_id.indexOf(txtName);
                    if (IndexId  != -1) {
                        if (txtPassword.equals(password.get(IndexId))) {
                            log=2;
                            admin=true;
                            toggleLogin(false);
                            btn_login.setEnabled(false);
                            toggleButtons(true);
                            JOptionPane.showMessageDialog(null, "Berhasil login sebagai admin");
                        } else {
                            JOptionPane.showMessageDialog(null, "Password salah");
                            pwd_password.setText("");
                        }
                    } else {
                        JOptionPane.showMessageDialog(null, "Akun tidak ditemukan");
                        clearLogin();
                    }
                } else {
                    JOptionPane.showMessageDialog(null, "Nama dan password tidak boleh kosong");
                }
            }
            
        }
    }//GEN-LAST:event_btn_loginActionPerformed

    private void btn_cancelActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btn_cancelActionPerformed
        // TODO add your handling code here:
        if (log==1) {
            log = 0;
            clearLogin();
            toggleLogin(false);
            btn_cancel.setEnabled(false);
        } else if (log==2) {
            log = 1;
            admin=false;
            clearLogin();
            toggleLogin(true);
            btn_login.setEnabled(true);
            toggleButtons(false);
        }
    }//GEN-LAST:event_btn_cancelActionPerformed

