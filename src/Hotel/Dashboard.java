    int log = 0;
    boolean admin = false;
    
    ArrayList<String> user_id = new ArrayList<String>();
    ArrayList<String> password = new ArrayList<String>();
    
    void clearLogin() {
        txt_userid.setText("");
        pwd_password.setText("");
        cek_guest.setSelected(false);
    }
    
    void toggleLogin(boolean a) {
        txt_userid.setEnabled(a);
        pwd_password.setEnabled(a);
        cek_guest.setEnabled(a);
    }
    
    void toggleButtons(boolean a) {
        if (a==true) {
            if (admin==true) {
                btn_guest.setEnabled(true);
                btn_resepsionis.setEnabled(true);
                btn_rooms.setEnabled(true);
                btn_management.setEnabled(true);
                btn_checkin.setEnabled(true);
                btn_pembayaran.setEnabled(true);
                btn_report.setEnabled(true);
                btn_checkout.setEnabled(true);
            } else {
                btn_checkin.setEnabled(true);
            }
        } else {
            btn_guest.setEnabled(false);
            btn_resepsionis.setEnabled(false);
            btn_rooms.setEnabled(false);
            btn_management.setEnabled(false);
            btn_checkin.setEnabled(false);
            btn_pembayaran.setEnabled(false);
            btn_report.setEnabled(false);
            btn_checkout.setEnabled(false);
        }
    }
    
